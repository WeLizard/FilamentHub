## SLA (Stereolithography)

### Как работает

UV-лазер или LCD-экран засвечивает жидкую фотополимерную смолу, затвердевая её слой за слоем.

### Преимущества

- ✅ **Высочайшая детализация** — можно печатать ювелирку
- ✅ **Гладкая поверхность** — слои почти не видны
- ✅ **Сложная геометрия** — идеально для миниатюр
- ✅ **Высокая скорость** — слой печатается целиком (MSLA)
- ✅ **Отличная точность** — ±0.05mm

### Недостатки

- ❌ **Токсичная смола** — нужны перчатки, маска, вентиляция
- ❌ **Постобработка** — промывка в изопропиловом спирте, УФ-засветка
- ❌ **Хрупкие детали** — смола более хрупкая чем PLA
- ❌ **Ограниченный размер печати** — обычно до 200x130x250mm
- ❌ **Высокая стоимость материалов** — смола дороже пластика
- ❌ **Срок годности смолы** — 6-12 месяцев

### Применение

**Идеально для:**
- Миниатюры и фигурки
- Ювелирные изделия (мастер-модели)
- Стоматология (модели, каппы)
- Высокодетализированные прототипы
- Художественные работы

### Стоимость

- **Принтер:** от $200 (ELEGOO Mars) до $3000+ (Formlabs)
- **Смола:** $30-80 за 1л
- **Эксплуатация:** изопропиловый спирт, перчатки, UV-лампа

