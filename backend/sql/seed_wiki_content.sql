## FDM (Fused Deposition Modeling)

### Как работает

Расплавленный пластик выдавливается через сопло, слой за слоем формируя объект.

### Преимущества

- ✅ **Простота использования** — загрузил пластик, запустил печать
- ✅ **Безопасность** — нет токсичных смол
- ✅ **Большой выбор материалов** — PLA, PETG, ABS, Nylon, TPU...
- ✅ **Крупные детали** — можно печатать большие объекты
- ✅ **Прочность** — функциональные детали
- ✅ **Низкая стоимость эксплуатации** — пластик дешевле смолы

### Недостатки

- ❌ **Видимые слои** — заметна слоистость
- ❌ **Ниже детализация** — сложно печатать мелкие детали
- ❌ **Supports** — поддержки оставляют следы
- ❌ **Медленная печать** — крупная деталь = часы печати

### Применение

**Идеально для:**
- Функциональные детали (крепления, механика)
- Прототипирование
- Бытовые предметы
- Крупные модели
- Обучение

### Стоимость

- **Принтер:** от $150 (Creality Ender-3) до $1000+ (Prusa, Bambu Lab)
- **Пластик:** $15-30 за 1кг
- **Эксплуатация:** очень дешевая

