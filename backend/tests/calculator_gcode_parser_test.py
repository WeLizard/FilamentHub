@pytest.mark.parametrize(
    ("banner", "expected_name"),
    [
        ("generated by OrcaSlicer 2.4.0", "OrcaSlicer"),
        ("generated by BambuStudio 2.3.0", "BambuStudio"),
        ("generated by PrusaSlicer 2.9.2", "PrusaSlicer"),
        ("generated by SuperSlicer 2.5.60", "SuperSlicer"),
        ("generated by CuraEngine 5.10.0", "Cura"),
        ("generated by Creality Print 6.1.0", "CrealitySlicer"),
    ],
)
def test_detects_supported_slicer_families(banner: str, expected_name: str) -> None:
    parsed = parse_gcode_payload("job.gcode", f"; {banner}\n; TIME:60\n".encode())

    assert parsed["slicer_name"] == expected_name


def test_gcode_3mf_lists_plates_and_parses_selected_plate() -> None:
    parsed = parse_gcode_payload("multi.gcode.3mf", _build_gcode_3mf(), plate_index=2)

    assert parsed["container_format"] == "gcode_3mf"
    assert parsed["plate_index"] == 2
    assert parsed["available_plate_indices"] == [1, 2]
    assert parsed["print_time_seconds"] == 300
    assert parsed["total_filament_weight_g"] == 8.5
    assert parsed["support_used"] is True
    assert parsed["thumbnail_data_url"].startswith("data:image/png;base64,")
    assert parsed["materials"][0]["tool_index"] == 1
    assert parsed["materials"][0]["settings_id"] == "PETG Support"
    assert parsed["materials"][0]["used_for_support"] is True
    assert parsed["materials"][0]["is_support_material"] is True


def test_gcode_3mf_uses_first_plate_by_default() -> None:
    parsed = parse_gcode_payload("multi.gcode.3mf", _build_gcode_3mf())

    assert parsed["plate_index"] == 1
    assert parsed["print_time_seconds"] == 120
    assert parsed["support_used"] is False
    assert parsed["materials"][0]["tool_index"] == 0
    assert parsed["infill_filament_weight_g"] == 3.15


def test_gcode_3mf_rejects_unknown_plate() -> None:
    with pytest.raises(ValueError, match="gcode_3mf_plate_not_found"):
        parse_gcode_payload("multi.gcode.3mf", _build_gcode_3mf(), plate_index=99)


def test_gcode_3mf_rejects_archive_without_sliced_plate() -> None:
    archive_bytes = io.BytesIO()
    with zipfile.ZipFile(archive_bytes, "w") as archive:
        archive.writestr("3D/3dmodel.model", "not sliced")

    with pytest.raises(ValueError, match="gcode_3mf_has_no_gcode"):
        parse_gcode_payload("model.gcode.3mf", archive_bytes.getvalue())


def test_gcode_3mf_ignores_unsafe_slice_info_xml() -> None:
    archive_bytes = io.BytesIO()
    with zipfile.ZipFile(archive_bytes, "w") as archive:
        archive.writestr("Metadata/plate_1.gcode", "; generated by OrcaSlicer 2.4.0\n")
        archive.writestr(
            "Metadata/slice_info.config",
            '<!DOCTYPE config [<!ENTITY x "unsafe">]><config><plate /></config>',
        )

    parsed = parse_gcode_payload("unsafe.gcode.3mf", archive_bytes.getvalue())

    assert parsed["container_format"] == "gcode_3mf"
    assert parsed["plate_index"] == 1
