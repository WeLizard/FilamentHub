def _build_gcode_3mf() -> bytes:
    archive_bytes = io.BytesIO()
    slice_info = """<?xml version="1.0" encoding="UTF-8"?>
<config>
  <plate>
    <metadata key="index" value="1" />
    <metadata key="prediction" value="120" />
    <metadata key="weight" value="4.20" />
    <metadata key="support_used" value="false" />
    <filament id="1" tray_info_idx="PLA Basic" type="PLA" color="#000000" used_m="1.40" used_g="4.20" used_for_object="true" used_for_support="false" />
  </plate>
  <plate>
    <metadata key="index" value="2" />
    <metadata key="prediction" value="300" />
    <metadata key="weight" value="8.50" />
    <metadata key="support_used" value="true" />
    <filament id="2" tray_info_idx="PETG Support" type="PETG" color="#FFFFFF" used_m="2.50" used_g="8.50" used_for_object="true" used_for_support="true" />
  </plate>
</config>
"""
    with zipfile.ZipFile(archive_bytes, "w", compression=zipfile.ZIP_DEFLATED) as archive:
        archive.writestr(
            "Metadata/plate_1.gcode",
            (
                "; generated by OrcaSlicer 2.4.0\n"
                "M83\n"
                ";TYPE:Sparse infill\n"
                "G1 X1 E3\n"
                ";TYPE:Outer wall\n"
                "G1 X2 E1\n"
            ),
        )
        archive.writestr(
            "Metadata/plate_2.gcode",
            "; generated by OrcaSlicer 2.4.0\n;TYPE:Support\n; total filament weight [g] : 0,8.50\n",
        )
        archive.writestr("Metadata/slice_info.config", slice_info)
        archive.writestr("Metadata/plate_2.png", b"\x89PNG\r\n\x1a\ncalculator-preview")
    return archive_bytes.getvalue()


